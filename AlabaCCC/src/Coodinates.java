public class Coodinates {
    protected Integer xPosition;
    protected Integer yPosition;

    public Coodinates(Integer xPosition, Integer yPostion) {
        this.xPosition = xPosition;
        this.yPosition = yPostion;
    }



    public Integer getxPosition() {
        return xPosition;
    }

    public void setxPosition(Integer xPosition) {
        this.xPosition = xPosition;
    }

    public Integer getyPostion() {
        return yPosition;
    }

    public void setyPostion(Integer yPostion) {
        this.yPosition = yPostion;
    }
}
